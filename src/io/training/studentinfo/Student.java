package io.training.studentinfo;

public class Student {
    String name, rollNo;

    public void setName(String name){
        this.name = name;
    }

    public void setRollNo(String rollNo){
        this.rollNo = rollNo;
    }

    public String getName(){
        return name;
    }

    public String getRollNo(){
        return rollNo;
    }
}
